PRAGMA user_version = 1;

CREATE TABLE valueType (
	id				INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT UNIQUE,
	code			VARCHAR2 NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS measure (
	id				INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT UNIQUE,
	code			VARCHAR2 NOT NULL UNIQUE,
	code_en			VARCHAR2,
	code_ru			VARCHAR2,
	valueTypeId		INTEGER
);

